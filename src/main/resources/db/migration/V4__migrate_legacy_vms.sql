-- =====================================================
-- Step 4: Create Environments (One per VM Name)
-- =====================================================

INSERT INTO environment (
    environment_id,
    name,
    display_name,
    description,
    is_active,
    created_at,
    updated_at,
    metadata
)
SELECT
    RANDOM_UUID() AS environment_id,
    LOWER(REGEXP_REPLACE(REGEXP_REPLACE(vm_name, '[^a-zA-Z0-9-]', '-'), '-+', '-')) AS name,
    vm_name AS display_name,
    vm_name AS description,
    TRUE AS is_active,
    create_date AS created_at,
    CURRENT_TIMESTAMP AS updated_at,
    CONCAT('{',
        '"ownerTeam":"CloudOps",',
        '"defaultCloudProvider":"AWS",',
        '"legacyVmId":', legacy_id, ',',
        '"legacyVmName":"', REPLACE(vm_name, '"', '\"'), '",',
        '"migratedAt":"', CURRENT_TIMESTAMP, '"',
    '}') AS metadata
FROM temp_vm_master
WHERE is_active = 'Y';

-- =====================================================
-- Step 5: Create Default Groups (One per Environment)
-- =====================================================

INSERT INTO vm_group (
    group_id,
    environment_id,
    name,
    display_name,
    description,
    sequence_position,
    depends_on_group_ids,
    created_at,
    updated_at,
    metadata
)
SELECT
    RANDOM_UUID() AS group_id,
    e.environment_id,
    'default-group' AS name,
    'Default Group' AS display_name,
    CONCAT('Default group for ', e.display_name) AS description,
    1 AS sequence_position,
    NULL AS depends_on_group_ids,
    e.created_at,
    CURRENT_TIMESTAMP AS updated_at,
    '{"isDefault":true,"legacyMigration":true}' AS metadata
FROM environment e
WHERE e.metadata LIKE '%legacyVmId%';

-- =====================================================
-- Step 6: Create VMs (One per Instance)
-- NOTE: Some instance IDs appear in multiple legacy VM records.
--       We keep only the FIRST occurrence (earliest legacy_id)
--       to avoid unique constraint violations.
-- =====================================================

INSERT INTO vm (
    vm_id,
    group_id,
    name,
    display_name,
    description,
    provider,
    region,
    provider_vm_id,
    vm_type,
    sequence_position,
    depends_on_vm_ids,
    status,
    last_known_state,
    last_state_sync_at,
    state_drift_detected,
    created_at,
    updated_at,
    metadata
)
SELECT
    RANDOM_UUID() AS vm_id,
    g.group_id,
    LOWER(deduped.instance_id) AS name,
    deduped.instance_id AS display_name,
    CONCAT('VM from ', deduped.vm_name, ' - Instance ', deduped.instance_sequence) AS description,
    'AWS' AS provider,
    'ap-south-1' AS region,
    deduped.instance_id AS provider_vm_id,
    'DEV' AS vm_type,
    deduped.instance_sequence AS sequence_position,
    NULL AS depends_on_vm_ids,
    'STOPPED' AS status,
    NULL AS last_known_state,
    NULL AS last_state_sync_at,
    FALSE AS state_drift_detected,
    deduped.create_date AS created_at,
    CURRENT_TIMESTAMP AS updated_at,
    CONCAT('{',
        '"legacyVmId":', deduped.legacy_id, ',',
        '"legacyVmName":"', REPLACE(deduped.vm_name, '"', '\"'), '",',
        '"instanceSequence":', deduped.instance_sequence, ',',
        '"isDuplicate":', CASE WHEN deduped.dup_count > 1 THEN 'true' ELSE 'false' END, ',',
        '"duplicateCount":', deduped.dup_count, ',',
        '"migratedAt":"', CURRENT_TIMESTAMP, '"',
    '}') AS metadata
FROM (
    -- Deduplicate: keep only the first occurrence of each instance_id
    SELECT
        ip.*,
        COUNT(*) OVER (PARTITION BY ip.instance_id) AS dup_count,
        ROW_NUMBER() OVER (PARTITION BY ip.instance_id ORDER BY ip.legacy_id, ip.instance_sequence) AS rn
    FROM temp_instance_parsed ip
) deduped
INNER JOIN environment e ON LOWER(REGEXP_REPLACE(REGEXP_REPLACE(deduped.vm_name, '[^a-zA-Z0-9-]', '-'), '-+', '-')) = e.name
INNER JOIN vm_group g ON g.environment_id = e.environment_id
WHERE e.metadata LIKE '%legacyVmId%'
  AND deduped.rn = 1  -- Only keep first occurrence
ORDER BY deduped.legacy_id, deduped.instance_sequence;

